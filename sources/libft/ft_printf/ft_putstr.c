#include"ft_printf.h"

int	ft_putstr(char *s)
{
	int	len_s;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	len_s = ft_strlen(s);
	write(1, s, len_s);
	return (len_s);
}
